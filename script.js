let currentQuestion = 0;
let score = 0;
let answered = false;
let userAnswers = [];

function loadQuestion() {
  answered = false;
  const q = questions[currentQuestion];
  
  document.getElementById('question-number').textContent = 
    `Domanda ${currentQuestion + 1} su ${questions.length}`;
  document.getElementById('question-text').textContent = q.question;
  document.getElementById('feedback').textContent = '';
  document.getElementById('feedback').className = 'feedback';
  document.getElementById('next-btn').classList.add('hidden');
  
  const grid = document.getElementById('answers-grid');
  grid.innerHTML = '';
  
  q.answers.forEach((answer, index) => {
    const btn = document.createElement('button');
    btn.className = 'answer-btn';
    btn.textContent = answer;
    btn.onclick = () => selectAnswer(index);
    grid.appendChild(btn);
  });
}

function selectAnswer(selectedIndex) {
  if (answered) return;
  
  answered = true;
  const q = questions[currentQuestion];
  const buttons = document.querySelectorAll('.answer-btn');
  
  userAnswers.push({
    question: q.question,
    userAnswer: selectedIndex,
    correctAnswer: q.correct,
    isCorrect: selectedIndex === q.correct
  });
  
  buttons.forEach((btn, index) => {
    btn.classList.add('disabled');
    if (index === q.correct) {
      btn.classList.add('correct');
    }
    if (index === selectedIndex && selectedIndex !== q.correct) {
      btn.classList.add('incorrect');
    }
  });
  
  const feedback = document.getElementById('feedback');
  if (selectedIndex === q.correct) {
    score++;
    feedback.textContent = 'corretto';
    feedback.className = 'feedback correct';

  } else {
    feedback.textContent = 'scorretto';
    feedback.className = 'feedback incorrect';
  }
  
  document.getElementById('score').textContent = `Punteggio: ${score}`;
  document.getElementById('next-btn').classList.remove('hidden');
}

document.getElementById('next-btn').onclick = function() {
  currentQuestion++;
  if (currentQuestion < questions.length) {
    loadQuestion();
  } else {
    showFinalScore();
  }
};

function showFinalScore() {
  document.getElementById('quiz-content').classList.add('hidden');
  const finalScreen = document.getElementById('final-screen');
  finalScreen.classList.remove('hidden');
  
  let resultsHTML = '<h2>Quiz Completato! 🎉</h2>';
  resultsHTML += `<div class="score-display">${score} / ${questions.length}</div>`;
  
  resultsHTML += '<div style="text-align: left; margin: 30px 0; max-height: 400px; overflow-y: auto;">';
  userAnswers.forEach((answer, index) => {
    const q = questions[index];
    const icon = answer.isCorrect ? '✅' : '❌';
    const color = answer.isCorrect ? '#00b894' : '#e74c3c';
    
    resultsHTML += `
      <div style="margin-bottom: 20px; padding: 15px; background: #f8f9fa; border-radius: 10px; border-left: 4px solid ${color};">
        <div style="font-weight: bold; margin-bottom: 8px; color: #2d3748;">
          ${icon} Domanda ${index + 1}: ${q.question}
        </div>
        <div style="margin-left: 20px; color: #4a5568;">
          ${answer.isCorrect 
            ? `<span style="color: ${color};">Risposta corretta: ${q.answers[q.correct]}</span>`
            : `<span style="color: ${color};">La tua risposta: ${q.answers[answer.userAnswer]}</span><br>
                <span style="color: #00b894;">Risposta corretta: ${q.answers[q.correct]}</span>`
          }
        </div>
      </div>
    `;
  });
  resultsHTML += '</div>';
  
  resultsHTML += '<button class="restart-btn" onclick="restartQuiz()">Gioca Ancora</button>';
  
  finalScreen.innerHTML = resultsHTML;
}

function restartQuiz() {
  currentQuestion = 0;
  score = 0;
  userAnswers = [];
  document.getElementById('quiz-content').classList.remove('hidden');
  document.getElementById('final-screen').classList.add('hidden');
  document.getElementById('score').textContent = 'Punteggio: 0';
  loadQuestion();
}

